package com.liuwa.web.vo;

/**
 * 缓存项
 */
public class CacheItemVo {

    /**
     * 名称
     */
    private String name;

    /**
     * 值
     */
    private String value;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
